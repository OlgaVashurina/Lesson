public class CamelCase {

    // psvm + Tab / Enter
    // main + Tab / Enter

//     Однострочный комментарий -> Ctrl + /

//    Строка 1 ком
//    Строка 2 ком
//    Строка 3 ком

    public static void main(String[] args) {
        // ТипПеременной имяПеременной;

        int myFirstVariable; // объявление (декларация) переменной. Имя переменной должно уникальной в данной части кода.

        // какие-то строки кода...

        myFirstVariable = 1; // присвоение значения переменной. Первое присвоение называется инициализацией.

        int mySecondVariable = 25; // объявление и инициализация (присвоение значения)

        System.out.println(mySecondVariable); // команда вывода на экран. В данном случае вывода значения переменной

        System.out.println("My second Var " + myFirstVariable );

    }

}
